int	ft_atoi(const char *a)
{
	int	n;
	int	minus;

	minus = 0;
	while (*a == ' ' || *a == '\t' || *a == '\n'
		|| *a == '\v' || *a == '\f' || *a == '\r')
		a++;
	if (*a == '-')
	{
		minus = 1;
		a++;
	}
	else if (*a == '+')
		a++;
	n = 0;
	while (*a >= '0' && *a <= '9')
	{
		n = n * 10 + (*a - '0');
		a++;
	}
	if (minus == 1)
		n = n * -1;
	return (n);
}
